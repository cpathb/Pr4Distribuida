    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String titulo = jTextField1.getText().trim();
            String precio = jTextField3.getText().trim();
            String minimo = jTextField4.getText().trim();
            String incremento = jTextField2.getText().trim();
            if(Integer.parseInt(precio)<=0){
                precio=""+1;
            }
            if(Integer.parseInt(incremento)<=0){
                incremento=""+1;
            }
            if(Integer.parseInt(precio)>Integer.parseInt(minimo)){
                minimo=""+(Integer.parseInt(precio)+Integer.parseInt(incremento));
            }
            Agente.crearSubasta(titulo,Integer.parseInt(precio), Integer.parseInt(incremento), Integer.parseInt(minimo));
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
        catch (Exception e){
        }
    }//GEN-LAST:event_jButton1ActionPerformed

