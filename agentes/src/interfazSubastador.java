    public void añadirMensaje(String mensaje){
        if(jTextArea1.getText().compareTo("")==0){
            jTextArea1.append(mensaje);
            jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength()); // para que scroll al final
        }
        else{
            jTextArea1.append("\n"+mensaje);
            jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength()); // para scroll al final
        }
    }
    
    public void listaSubastas(DefaultListModel<String> lista){
        try {
            DefaultListModel<String> model = (DefaultListModel) jList1.getModel();
            model.removeAllElements();
            jList1.setModel(model);
        }
        catch(Exception e){}
        jList1.setModel(lista);
        jList1.repaint();
    }
    
    
    
